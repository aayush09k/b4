    /// Create Digital Signature
    ///
    /// This function is used to take data input from the user and  create a digital signature by using
    /// user's private key and return the digital signature in String format.
    Future<String> createDigitalSignature(String content, privateKeyPemDecrypted) async
    {
        Uint8List dataToSign = Uint8List.fromList(utf8.encode(content));
        final privateKey = crypto_utils.CryptoUtils.ecPrivateKeyFromPem(privateKeyPemDecrypted);
        final digSign = crypto_utils.CryptoUtils.ecSign(privateKey, dataToSign);
        final digitalSignature = crypto_utils.CryptoUtils.ecSignatureToBase64(digSign);
        return digitalSignature;
    }



