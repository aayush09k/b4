/// checkMessageForAuthentication
    ///
    /// This function will be used to check the integrity of the content in the message if the content is not tampered then it will return the string
    /// else it will give null.
    Future<String?> checkMessageForAuthentication(String message, userCert) async {
      try {
        // Parse the JSON message to extract the signature and content
        Map<String, dynamic> messageJson = jsonDecode(message);
        String signature = messageJson["signature"];
        String content = messageJson["content"];
        Uint8List contentBytes = utf8.encode(content);
        final publicKeyPem = await extractPublicKeyFromCertificate(userCert);
        final publicKey = crypto_utils.CryptoUtils.ecPublicKeyFromPem(publicKeyPem);
        bool isSignatureValid = crypto_utils.CryptoUtils.ecVerifyBase64(publicKey ,contentBytes,signature);
        // If the signature is valid, return the content; otherwise, return null
        if (isSignatureValid) {
          return content;
        } else {
          return null;
        }
      } catch (e) {
        // Handle JSON parsing errors or signature verification errors
        print("Error checking message: $e");
        return null;
      }
    }




    /// validateTime
    ///
    /// The purpose of this function is to access the signed certificate from secure storage and check it's validity
    /// weather the certificate has expired or not. Validity is for 365 days, after that the user needs to again
    /// generate a new certificate and get it signed from server. It returns a isValid bool value which is true only if
    /// the certificate has not expired. If the validity has expired the application
    /// will not start functioning  properly till the time the certificate has been renewed.
    ///
    Future<bool> validateTime(String decryptedServerSignedCertificate) async {
        try {
            final validityDuration = x509.X509Utils.x509CertificateFromPem(decryptedServerSignedCertificate);
            final creationDateTime = validityDuration.tbsCertificate?.validity.notBefore;
            final expiryDateTime = validityDuration.tbsCertificate?.validity.notAfter;

            if (creationDateTime != null && expiryDateTime != null) {
                // Print creation and expiry date times
                print('Certificate Creation Date and Time: $creationDateTime');
                print('Certificate Expiry Date and Time: $expiryDateTime');

                // Check if the current date and time are within the validity period
                final currentDate = DateTime.now();
                final isValid = currentDate.isAfter(creationDateTime) && currentDate.isBefore(expiryDateTime);

                // Print whether the certificate is valid or not
                //print('Is Certificate Valid: $isValid');

                if (isValid) {
                    // Calculate and print the remaining validity duration in days
                    final remainingDays = max(0, expiryDateTime.difference(currentDate).inDays);
                    print('Remaining Validity Duration: $remainingDays days');
                }

                return isValid;
            }

            if (kDebugMode) {
                print('Error during validity check: Invalid decryptedValues.');
            }
            return false;
        } catch (e) {

            if (kDebugMode) {
                print('Error during validity check: $e');
            }
            return false;
        }
    }


